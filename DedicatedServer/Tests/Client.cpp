// Client side C/C++ program to demonstrate Socket programming 
#include <unistd.h> 
#include <stdio.h> 
#include <sys/socket.h> 
#include <stdlib.h> 
#include <netinet/in.h> 
#include <string.h> 
#include <arpa/inet.h>

#define PORT 8080 
   
int main(int argc, char const *argv[]) 
{ 
    struct sockaddr_in address; 
    int sock = 0, valread; 
    struct sockaddr_in serv_addr; 

    char header[12], body[1024], checkSum[25];
    char *tiles = (char*)"|KEY:VALUE#KEY2:VALUE2|";

    memset(&header, 0xFF, 12);
    memset(&body, 0xFF, 1024);
    memset(&checkSum, 0xFF, 25);

    memcpy(&header, "T:T", strlen("T:T,B:24"));
    memcpy(&body, tiles, strlen(tiles));
    memcpy(&checkSum, "OKAY200", strlen("OKAY200"));

    char * hello = new char[strlen(header)+strlen(checkSum)+strlen(body)+1];
    strcpy(hello,header);
    strcat(hello,checkSum);
    strcat(hello,body);

    char buffer[1061] = {0}; 
    if ((sock = socket(AF_INET, SOCK_STREAM, 0)) < 0) 
    { 
        printf("\n Socket creation error \n"); 
        return -1; 
    } 
   
    memset(&serv_addr, '0', sizeof(serv_addr)); 
   
    serv_addr.sin_family = AF_INET; 
    serv_addr.sin_port = htons(PORT); 
       
    // Convert IPv4 and IPv6 addresses from text to binary form 
    if(inet_pton(AF_INET, "127.0.0.1", &serv_addr.sin_addr)<=0)  
    { 
        printf("\nInvalid address/ Address not supported \n"); 
        return -1; 
    } 
   
    if (connect(sock, (struct sockaddr *)&serv_addr, sizeof(serv_addr)) < 0) 
    { 
        printf("\nConnection Failed \n"); 
        return -1; 
    } 
    send(sock , hello , strlen(hello) , 0 ); 
    printf("Hello message sent\n"); 
    valread = read( sock , buffer, 1024); 
    printf("%s\n",buffer ); 
    return 0; 
} 